Menu::Menu(Adafruit_SSD1306* displayPtr){
    this->display = displayPtr;
}

void Menu::yellowSquare() {
  display->clearDisplay();
  display->fillRect(0, 0, 128, 16, SSD1306_WHITE);
  display->setTextSize(1);
  display->setTextColor(SSD1306_BLACK);
  display->setCursor(4, 4);
  display->print("TAMAGOTCHI");
  display->setTextColor(SSD1306_WHITE);
  display->setCursor(15, 40);
  display->print("Benjamin Constant");
  display->display();
} 

void Menu::showMenu() {
  display->clearDisplay();
  display->drawBitmap(0, 0, menu_base, 128, 16, SSD1306_WHITE);
  display->display();
}

void Menu::setOption(int option) {
  if (option < 0 || option > 4) return; // Invalid option
  this->currentOption = option;
}

void Menu::draw() {
    display->clearDisplay();

    switch(currentOption) {
        case 0: // État neutre
            display->drawBitmap(0, 0, menu_base, 128, 16, SSD1306_WHITE);
            break;
        case 1: // Focus sur Nourrir
            display->drawBitmap(0, 0, menu_feed, 128, 16, SSD1306_WHITE);
            break;
        case 2: // Focus sur Nettoyer
            display->drawBitmap(0, 0, menu_clean, 128, 16, SSD1306_WHITE);
            break;
        case 3: // Focus sur Santé
            display->drawBitmap(0, 0, menu_play, 128, 16, SSD1306_WHITE);
            break;
        case 4: // Focus sur Jouer
            display->drawBitmap(0, 0, menu_love, 128, 16, SSD1306_WHITE);
            break;
    }

    display->display();
}